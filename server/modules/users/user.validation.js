const validateEmail = (email) => {
  return /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/.test(email);
};
const validatePassword = (password) => {
  if (
    password.length <= 8 ||
    !password.match(numbers) ||
    !password.match(cap_letters) ||
    !password.match(letters)
  ) {
    return "weak password";
  }
};

